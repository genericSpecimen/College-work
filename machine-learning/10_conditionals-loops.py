a = 10
b = 20

if a < b:
    print("a < b\n")
elif a == b:
    print("a == b\n")
else:
    print("a > b\n")

# for loop
print("For loop that iterates from 0 to 9")
for x in range(0,10):
    print(f"x = {x}")

# while loop
y = 10
print("\nWhile loop that iterates from 10 to 1")
while y > 0:
    print(f"y = {y}")
    y -= 1


